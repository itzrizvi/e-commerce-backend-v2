// ATTR BASED QUERY
const { getAllAttributeController, getSingleAttributeController } = require("../../controllers");

// ATTR QUERIES
module.exports = {
    // GET ALL ATTR
    getAllAttributes: async (root, args, { db, user, isAuth, TENANTID }, info) => {
        // Return If Not Have TENANT ID
        if (!TENANTID || TENANTID == "undefined") return { message: "TENANT ID IS MISSING!!!", status: false }
        // Return If No Auth
        if (!user || !isAuth) return { message: "Not Authorized", status: false };
        if (user.has_role === '0') return { message: "Not Authorized", status: false };

        // Return To Controller
        return await getAllAttributeController(db, user, isAuth, TENANTID);
    },
    // GET SINGLE ATTR
    getSingleAttribute: async (root, args, { db, user, isAuth, TENANTID }, info) => {
        // Return If Not Have TENANT ID
        if (!TENANTID || TENANTID == "undefined") return { message: "TENANT ID IS MISSING!!!", status: false }
        // Return If No Auth
        if (!user || !isAuth) return { message: "Not Authorized", status: false };
        if (user.has_role === '0') return { message: "Not Authorized", status: false };

        // Return To Controller
        return await getSingleAttributeController(args.query, db, user, isAuth, TENANTID);
    },

}